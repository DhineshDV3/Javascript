let number_1 = 5;
let number_2 = 20;

/*-----------------------------------------------------------------------------------------------------------------*/

//  Increment (++)          Answer
//      Before
console.log(++number_1);    //  6   => Displays the value with incremention
console.log(number_1);      //  6   => Displays the (++number_1) value

//      After
console.log(number_2++);    //  20  => Increment the value
console.log(number_2);      //  21  => Displays the Incremented (number_2++) Value

//  Decrement (--)
//      Before
console.log(--number_1);    //  5   => Displays the value with decremention
console.log(number_1);      //  5   => Displays the (--number_1) value

//      After
console.log(number_2--);    //  21  => Decrement the value
console.log(number_2);      //  20  => Displays the Decremented (number_2--) Value

/*-----------------------------------------------------------------------------------------------------------------*/

//          Addition
console.log("Addition = ", number_1 + number_2);

//          Subtraction
console.log("Subtraction = ", number_1 - number_2);

//          Multiplication
console.log("Multiplication = ", number_1 * number_2);

//          Division
console.log("Division = ", number_1 / number_2);

//          Remainder of Division (%)
console.log("Remainder = ", number_1 % number_2);

//          Exponential (**)
console.log("Exponential = ", number_1 ** number_2);

